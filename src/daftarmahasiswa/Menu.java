    public final DefaultTableModel setTable() {
        Object[] column = {"NIM", "Nama", "Nilai"};
        DefaultTableModel dataTabel = new DefaultTableModel(null, column);
        for (int i = 0; i < listMhs.size(); i++) {
            Object[] row = new Object[3];
            row[0] = listMhs.get(i).getNim();
            row[1] = listMhs.get(i).getNama();
            row[2] = listMhs.get(i).getNilai();

            dataTabel.addRow(row);
        }

        return dataTabel;
    }
    
    public void resetTextField() {
        //untuk mengosongkan textfield
        txtf_nim.setText("");
        txtf_nama.setText("");
        txtf_nilai.setText("");
    }

    public void insertData() {
        // Get Data
        String nim = txtf_nim.getText();
        String nama = txtf_nama.getText();
        String nilai = txtf_nilai.getText();
        
        if((nim.equals("")) || (nama.equals("")) || nilai.equals("")){
            JOptionPane.showMessageDialog(null, "Data tidak boleh ada yang kosong!");
        }else{
            listMhs.add(new Mahasiswa(nim, nama, nilai));
            //Update Table
            table_mahasiswa.setModel(setTable());
            
            resetTextField();
            System.out.println("Insert Success!");
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
        }
    }

    public void updateData() {
        // Get Data
        String nim = txtf_nim.getText();
        String nama = txtf_nama.getText();
        String nilai = txtf_nilai.getText();
        //System.out.println(nim + nama + nilai);

        listMhs.get(selectedID).setNim(nim);
        listMhs.get(selectedID).setNama(nama);
        listMhs.get(selectedID).setNilai(nilai);
        
        //Update Table
        table_mahasiswa.setModel(setTable());
        
        resetTextField();
        System.out.println("Update Success!");
        JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
    }

    public void deleteData() {
        //buat array data mahasiswa (sementara)
        ArrayList<Mahasiswa> temp_listMhs = new ArrayList<>();
        
        for (int i = 0; i < listMhs.size(); i++) {
            if(selectedID != i){
                /* masukkan semua data ke array sementara 
                kecuali data pada baris yang akan di hapus */
                String nim = listMhs.get(i).getNim();
                String nama = listMhs.get(i).getNama();
                String nilai = listMhs.get(i).getNilai();
                temp_listMhs.add(new Mahasiswa(nim, nama, nilai));
            }
        }
        
        //masukkan array data mahasiswa baru
        listMhs = temp_listMhs;
        
        //Update Table
        table_mahasiswa.setModel(setTable());
        
        resetTextField();
        System.out.println("Delete Success!");
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
    }

