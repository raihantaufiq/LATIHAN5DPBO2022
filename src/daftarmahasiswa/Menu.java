    private void table_mahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_mahasiswaMouseClicked
        // TODO add your handling code here:
        this.isUpdate = true;

        //Get Selected Data
        int row = table_mahasiswa.getSelectedRow();
        String selectedNim = (table_mahasiswa.getModel().getValueAt(row, 0).toString());
        String selectedNama = (table_mahasiswa.getModel().getValueAt(row, 1).toString());
        String selectedNilai = (table_mahasiswa.getModel().getValueAt(row, 2).toString());

        for (int i = 0; i < listMhs.size(); i++) {
            if (selectedNim.equals(listMhs.get(i).getNim())) {
                selectedID = i;
                break;
            }
        }

        //System.out.println(selectedID);
        txtf_nim.setText(selectedNim);
        txtf_nama.setText(selectedNama);
        txtf_nilai.setText(selectedNilai);

        btn_add.setText("Update");
        btn_delete.setVisible(true);
    }//GEN-LAST:event_table_mahasiswaMouseClicked

